const hre = require("hardhat");
const fs = require("fs");
const path = require("path");

// Load contract address from deployment
const ORACLE_ADDRESS = process.env.ORACLE_ADDRESS || "";

// Load Pinata JWT for IPFS uploads
function getPinataJWT() {
  try {
    const credsPath = path.join(process.env.HOME, '.openclaw', 'credentials', 'pinata.json');
    const creds = JSON.parse(fs.readFileSync(credsPath, 'utf8'));
    return creds.jwt;
  } catch (e) {
    return null;
  }
}

async function uploadToIPFS(data, name) {
  const jwt = getPinataJWT();
  if (!jwt) {
    console.log('  ⚠️  No Pinata JWT, skipping IPFS upload');
    return `no-ipfs-${Date.now()}`;
  }

  const res = await fetch('https://api.pinata.cloud/pinning/pinJSONToIPFS', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${jwt}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      pinataContent: data,
      pinataMetadata: { name }
    })
  });

  if (!res.ok) {
    console.log(`  ⚠️  IPFS upload failed: ${res.status}`);
    return `ipfs-failed-${Date.now()}`;
  }

  const result = await res.json();
  return result.IpfsHash;
}

async function main() {
  if (!ORACLE_ADDRESS) {
    console.error("Set ORACLE_ADDRESS in .env");
    process.exit(1);
  }

  const [signer] = await hre.ethers.getSigners();
  const oracle = await hre.ethers.getContractAt("ClawOracle", ORACLE_ADDRESS, signer);

  // Load analysis results
  const resultsPath = process.argv[2] || "analysis-results.json";
  if (!fs.existsSync(resultsPath)) {
    console.error(`No results file: ${resultsPath}`);
    console.log("Run: node scripts/analyze-yields.js --output analysis-results.json");
    process.exit(1);
  }

  const results = JSON.parse(fs.readFileSync(resultsPath));
  console.log(`Recording ${results.length} attestations on-chain...`);
  console.log(`Network: ${hre.network.name}`);
  console.log(`Agent: ${signer.address}`);
  console.log("---");

  for (const r of results) {
    try {
      // Upload full reasoning to IPFS
      const reasoningData = {
        agent: "Claw (OpenClaw)",
        timestamp: new Date().toISOString(),
        protocol: r.protocol,
        chain: r.chain,
        asset: r.asset,
        apy: r.apy,
        apyBps: r.apyBps,
        tvl: r.tvl,
        riskScore: r.riskScore,
        decision: r.decision,
        confidence: r.confidence,
        factors: r.factors || [],
        stablecoin: r.stablecoin || false,
        ilRisk: r.ilRisk || "unknown",
        methodology: "Automated scan via DeFiLlama API. Risk scoring: base 5, +1 for TVL>$100M, +1 for stablecoin, +1 for no IL risk, -3 for APY>100% (suspicious). Decision: >=7 RECOMMEND, >=4 CAUTION, <4 AVOID.",
        disclaimer: "Generated by Claw, an AI agent. Not financial advice. DYOR.",
        source: "DeFiLlama API (https://yields.llama.fi/pools)"
      };

      const ipfsName = `claw-attestation-${r.protocol}-${r.asset}-${Date.now()}`;
      console.log(`  📤 Uploading reasoning to IPFS...`);
      const reasoningHash = await uploadToIPFS(reasoningData, ipfsName);
      console.log(`  📦 IPFS: ${reasoningHash}`);

      const tx = await oracle.record(
        r.protocol,
        r.chain,
        r.asset,
        r.apyBps,
        r.riskScore,
        reasoningHash,
        r.decision,
        r.confidence
      );
      
      const receipt = await tx.wait();
      console.log(`  ✅ ${r.protocol} | ${r.asset} | Risk ${r.riskScore}/10 | ${r.decision} | tx: ${receipt.hash}`);
    } catch (e) {
      console.log(`  ❌ ${r.protocol} failed: ${e.message}`);
    }
  }

  const total = await oracle.count();
  console.log(`\n📊 Total on-chain attestations: ${total}`);
}

main().catch(console.error);
